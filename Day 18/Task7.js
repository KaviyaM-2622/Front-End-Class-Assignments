let color = "red"; 

switch (color.toLowerCase()) {
  case "blue":
    console.log("Blue is my favorite color");
    break;
  case "black":
    console.log("Black is my favorite color");
    break;
  case "green":
    console.log("Green is my favorite color");
    break;
  case "red":
    console.log("Red is my favorite color");
    break;
  case "white":
    console.log("White is my favorite color");
    break;

}
